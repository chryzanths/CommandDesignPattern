package Receiver;

public class Thermostat
{
    public String activate()
    {
        return "Thermostat was successfully activated";
    }

    public String deactivate()
    {
        return "Thermostat was successfully deactivated";
    }
    public String tempUp()
    {
        return "Thermostat's temperature was increased";
    }
    public String tempDown()
    {
        return "Thermostat's temperature was decreased";
    }
}
